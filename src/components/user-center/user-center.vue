<template>
  <transition name="slide">
  <div class="user-center">
    <div class="back" @click="back">
      <i class="icon-back"></i>
    </div>
    <div class="switches-wrapper">
      <switches @changeIndex="changeIndex" :switches="switches" :currentIndex="currentIndex"></switches>
    </div>
    <div class="play-btn" @click="random" ref="playBtn">
      <i class="icon-play"></i>
      <span class="text">随机播放全部</span>
    </div>
    <div class="list-wrapper" ref="listWrapper">
      <scroll :data="favoriteList" ref="favoriteList" class="list-scroll" v-if="currentIndex === 0">
          <div class="list-inner">
            <song-list :songs="favoriteList" @select="selectSong"></song-list>
          </div>
        </scroll>
        <scroll ref="playList" v-if="currentIndex === 1" :data="playHistory" class="list-scroll">
        <div class="list-inner">
            <song-list :songs="playHistory" @select="selectSong"></song-list>
        </div>
      </scroll>
    </div>
    <div v-show="noResult" class="no-result-wrapper">
      <no-result :title="noResultDesc"></no-result>
    </div>
  </div>
  </transition>
</template>

<script>
import Switches from 'components/base/switches/switches'
import scroll from 'components/base/scroll/scroll'
import songList from 'components/song-list/song-list'
import noResult from 'components/base/no-result/no-result'

import {mapGetters, mapActions} from 'vuex'
import {playListMixin} from 'common/js/mixin'
export default {
  mixins: [playListMixin],
  components: {
    Switches,
    scroll,
    songList,
    noResult
  },
  data() {
    return {
      switches: [{
        id: 1,
        name: '我喜欢的'
      }, {
        id: 2,
        name: '最近听的'
      }],
      currentIndex: 0
    }
  },
  methods: {
    handlePlayList(playList) {
      const bottom = playList.length > 0 ? '60px' : ''
      console.log(playList)
      this.$refs.listWrapper.style.bottom = bottom
      this.$refs.favoriteList && this.$refs.favoriteList.refresh()
      this.$refs.playList && this.$refs.playList.refresh()
    },
    back() {
      this.$router.back()
    },
    
    changeIndex(index) {
      this.currentIndex = index
    },
    selectSong(song) {
      this.insertSong(song)
    },
    random() {
      let list = this.currentIndex === 0 ? this.favoriteList : this.playHistory
      if (list.length === 0) return
      this.randomPlay({list})
    },
    ...mapActions([
      'insertSong',
      'randomPlay'
    ]),
  },
  computed: {
    noResult() {
      if (this.currentIndex === 0) {
          return !this.favoriteList.length
      } else {
          return !this.playHistory.length
      }
    },
    noResultDesc() {
      if (this.currentIndex === 0) {
          return '暂无收藏歌曲'
      } else {
          return '你还没有听过歌曲'
      }
    },
    ...mapGetters([
      'favoriteList',
      'playHistory'
    ]),
  }
}
</script>

